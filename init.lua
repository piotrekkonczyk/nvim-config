              -- require('luasnip.loaders.from_vscode').lazy_load()
              require('luasnip.loaders.from_lua').load { paths = '~/.config/nvim/lua/snippets/' }
            end,
          },
        },
        opts = {},
      },
      'folke/lazydev.nvim',
    },

